
#Sum 2 numbers
def sum(a,b):
    return  a + b 

#sub 2 numbers
def sub(a,b):
    return a - b

#mult 2 numbers
def mul(a,b):
    return a * b

#div 2 number
def div(a,b):
    if a == 0 or b == 0:
        print('There are no divisions by 0')

    else:
        return a / b
    
